package kadai31;

public class WordBean {
	private String eng;
	private String jpn;

	public WordBean() {

	}

	public WordBean(String eng, String jpn) {
		this.eng = eng;
		this.jpn = jpn;
	}

	public String getEng() {
		return eng;
	}

	public void setEng(String eng) {
		this.eng = eng;
	}

	public String getJpn() {
		return jpn;
	}

	public void setJpn(String jpn) {
		this.jpn = jpn;
	}

}
